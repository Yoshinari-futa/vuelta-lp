/**
 * public 配下の表示用画像を最大幅 1400px に収め、PNG を再圧縮。
 * 続けて lib/blurPlaceholders.ts を生成（placeholder="blur" 用）。
 *
 * 実行: node scripts/optimize-public-images.mjs
 */
import sharp from 'sharp'
import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.join(__dirname, '..')
const PUBLIC = path.join(ROOT, 'public')

const MAX_WIDTH = 1400
const BLUR_SIZE = 12

/** LP で参照されるパス（これ以外は触らない） */
const FILES = [
  'images/hero.png',
  'images/interior.png',
  'images/manager-yuta.png',
  'images/vuelta-logo.png',
  'images/cocktails/shellwe.png',
  'images/cocktails/okonomiyaki.png',
  'images/cocktails/26hours.png',
  'images/cocktails/sakura-margarita.png',
]

async function optimizeFile(rel) {
  const abs = path.join(PUBLIC, rel)
  if (!fs.existsSync(abs)) {
    console.warn('skip (missing):', rel)
    return null
  }
  const meta = await sharp(abs).metadata()
  if (!meta.width) return abs
  // 幅が MAX_WIDTH を超えるときだけリサイズ（無闇に再エンコードしない）
  if (meta.width <= MAX_WIDTH) {
    console.log('skip resize:', rel, meta.width, '×', meta.height, fs.statSync(abs).size, 'bytes')
    return abs
  }
  const tmp = abs + '.opt.tmp'
  await sharp(abs)
    .resize(MAX_WIDTH, null, {
      withoutEnlargement: true,
      fit: 'inside',
    })
    .png({ compressionLevel: 9, adaptiveFiltering: true, effort: 10 })
    .toFile(tmp)
  fs.renameSync(tmp, abs)
  const after = await sharp(abs).metadata()
  console.log('resized:', rel, '→', after.width, '×', after.height, fs.statSync(abs).size, 'bytes')
  return abs
}

async function makeBlurDataURL(abs) {
  const buf = await fs.promises.readFile(abs)
  const blurBuf = await sharp(buf)
    .resize(BLUR_SIZE, BLUR_SIZE, { fit: 'cover' })
    .jpeg({ quality: 45, mozjpeg: true })
    .toBuffer()
  return `data:image/jpeg;base64,${blurBuf.toString('base64')}`
}

async function main() {
  for (const rel of FILES) {
    await optimizeFile(rel)
  }

  const entries = {}
  for (const rel of FILES) {
    const abs = path.join(PUBLIC, rel)
    if (!fs.existsSync(abs)) continue
    const webPath = '/' + rel.split(path.sep).join('/')
    entries[webPath] = await makeBlurDataURL(abs)
  }

  const outPath = path.join(ROOT, 'lib', 'blurPlaceholders.ts')
  const body = `// AUTO-GENERATED by scripts/optimize-public-images.mjs — do not edit by hand
export const BLUR_DATA_URL: Record<string, string> = ${JSON.stringify(entries, null, 2)}

const BLUR_FALLBACK =
  'data:image/jpeg;base64,/9j/4AAQSkZJRgABAQAAAQABAAD/2wBDAAgGBgcGBQgHBwcJCQgKDBQNDAsLDBkSEw8UHRofHh0aHBwgJC4nICIsIxwcKDcpLDAxNDQ0Hyc5PTgyPC4zNDL/2wBDAQkJCQwLDBwNDRwyIRwhMjIyMjIyMjIyMjIyMjIyMjIyMjIyMjIyMjIyMjIyMjIyMjIyMjIyMjIyMjIyMjIyMjL/wAARCAABAAEDASIAAhEBAxEB/8QAFQABAQAAAAAAAAAAAAAAAAAAAAv/xAAUEAEAAAAAAAAAAAAAAAAAAAAA/8QAFQEBAQAAAAAAAAAAAAAAAAAAAAX/xAAUEQEAAAAAAAAAAAAAAAAAAAAA/9oADAMBAAIRAxEAPwCwAA8A/9k='

export function blurDataUrl(src: string): string {
  const u = BLUR_DATA_URL[src]
  if (!u) {
    console.warn('[blurPlaceholders] Missing blur for:', src)
    return BLUR_FALLBACK
  }
  return u
}
`
  fs.writeFileSync(outPath, body, 'utf8')
  console.log('wrote', outPath)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
